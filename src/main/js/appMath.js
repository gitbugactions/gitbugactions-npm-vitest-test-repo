class AppMath {
    sum(a, b) {
        return a + b;
    }

    subtract(a, b) {
        return a - b;
    }

    multiply(a, b) {
        return a * b;
    }

    sumTwice(a, b) {
        return a + b + b;
    }

    subtractTwice(a, b) {
        return a - b - b;
    }
}

module.exports = AppMath;
